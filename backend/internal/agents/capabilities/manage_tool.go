// ManageInput is the input schema for the capability_manage tool.
type ManageInput struct {
	Action string `json:"action"` // tree|inspect|validate|diagnose|generate_prompt|generate_allowlist
	// tree/inspect params
	NodeID string `json:"node_id,omitempty"` // Node ID for inspect, subtree root for tree
	Depth  int    `json:"depth,omitempty"`   // Max depth for tree (0 = unlimited)
	// generate_prompt/generate_allowlist params
	Tier string `json:"tier,omitempty"` // Intent tier: greeting/question/task_light/task_write/task_delete/task_multimodal
	// validate params
	Level int `json:"level,omitempty"` // Validation level: 1/2/3 (0 = all levels)
	// P7: propose_register params
	NodeSpec *ProposeNodeSpec `json:"node_spec,omitempty"` // Node specification for propose_register
	// P7: propose_update/routing/binding params
	Updates json.RawMessage `json:"updates,omitempty"` // Partial fields to update
	// P7: apply_patch params
	PatchID  string `json:"patch_id,omitempty"` // Patch ID for apply_patch
	Approved bool   `json:"approved,omitempty"` // Approval flag for apply_patch (exec_escalation)
}

// ManageResult is the output of the capability_manage tool.
type ManageResult struct {
	Action  string      `json:"action"`
	Success bool        `json:"success"`
	Data    interface{} `json:"data,omitempty"`
	Error   string      `json:"error,omitempty"`
}

// ---------------------------------------------------------------------------
// P7-6: Patch diff format (simplified JSON Patch for capability tree)
