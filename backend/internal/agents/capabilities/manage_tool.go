// ---------------------------------------------------------------------------

// DiagnoseResult holds the drift diagnosis output.
type DiagnoseResult struct {
	Inventory string   `json:"inventory"`
	Checks    []string `json:"checks"`
}

func executeManageDiagnose(tree *CapabilityTree) ManageResult {
	var checks []string

	staticCount := len(tree.AllStaticTools())
	dynamicGroups := tree.DynamicGroups()
	groupCount := 0
	tree.Walk(func(n *CapabilityNode) bool {
		if n.Kind == NodeKindGroup && (n.Runtime == nil || !n.Runtime.Dynamic) {
			groupCount++
		}
		return true
	})

	// Build dynamic group summary
	var dynParts []string
	for _, g := range dynamicGroups {
		prefix := ""
		if g.Runtime != nil {
			prefix = g.Runtime.NamePrefix
		}
		dynParts = append(dynParts, prefix)
	}
	sort.Strings(dynParts)
	inventory := fmt.Sprintf("%d static + %d dynamic groups (%s) + %d groups",
		staticCount, len(dynamicGroups), strings.Join(dynParts, "/"), groupCount)

	// D1: prompt summaries alignment
	summaries := tree.ToolSummaries()
	allTools := tree.AllStaticTools()
	d1Aligned := 0
	for _, name := range allTools {
		if _, ok := summaries[name]; ok {
			d1Aligned++
		}
	}
	if d1Aligned == len(allTools) {
		checks = append(checks, fmt.Sprintf("✓ D1 prompt summaries: %d/%d aligned", d1Aligned, len(allTools)))
	} else {
		checks = append(checks, fmt.Sprintf("⚠ D1 prompt summaries: %d/%d aligned (%d missing)", d1Aligned, len(allTools), len(allTools)-d1Aligned))
	}

	// D2: frontend tool policy
	d2ProfileCount := 0
	d2GroupCount := 0
	for _, n := range tree.Nodes {
		if n.Kind == NodeKindGroup || n.Policy == nil {
			continue
		}
		if len(n.Policy.Profiles) > 0 {
			d2ProfileCount++
		}
		if len(n.Policy.PolicyGroups) > 0 {
			d2GroupCount++
		}
	}
	checks = append(checks, fmt.Sprintf("✓ D2 frontend tool policy: %d tools with profiles, %d with policy groups", d2ProfileCount, d2GroupCount))

	// D3: intent allowlists per tier
	tiersOK := 0
	for _, tier := range validTiers {
		allow := tree.AllowlistForTier(tier)
		if allow != nil {
			tiersOK++
		}
	}
	checks = append(checks, fmt.Sprintf("✓ D3 intent allowlists: %d/%d tiers computed", tiersOK, len(validTiers)))

	// D4: frontend tool display coverage
	d4WithDisplay := 0
	d4WithoutDisplay := 0
	for _, n := range tree.Nodes {
		if n.Kind == NodeKindGroup {
			continue
		}
		if n.Display != nil && n.Display.Title != "" {
			d4WithDisplay++
		} else {
			d4WithoutDisplay++
		}
	}
	if d4WithoutDisplay == 0 {
		checks = append(checks, fmt.Sprintf("✓ D4 frontend tool display: %d/%d tools have display specs", d4WithDisplay, d4WithDisplay))
	} else {
		checks = append(checks, fmt.Sprintf("⚠ D4 frontend tool display: %d tools have display, %d missing", d4WithDisplay, d4WithoutDisplay))
	}

	// D5: backend policy groups
	policyGroups := tree.PolicyGroups()
	checks = append(checks, fmt.Sprintf("✓ D5 backend policy groups: %d groups defined", len(policyGroups)))

	// D6: frontend wizard config
	d6WithWizard := 0
	for _, n := range tree.Nodes {
		if n.Kind == NodeKindGroup || n.Policy == nil {
			continue
		}
		if n.Policy.WizardGroup != "" {
			d6WithWizard++
		}
	}
	wizardGroups := tree.WizardGroups()
	checks = append(checks, fmt.Sprintf("✓ D6 frontend wizard config: %d tools assigned to %d wizard groups", d6WithWizard, len(wizardGroups)))

	// D7: display specs
	displaySpecs := tree.DisplaySpecs()
	d7Missing := 0
	for _, name := range allTools {
		if _, ok := displaySpecs[name]; !ok {
			d7Missing++
		}
	}
	if d7Missing == 0 {
		checks = append(checks, fmt.Sprintf("✓ D7 tool display: %d/%d have display specs", len(displaySpecs), len(allTools)))
	} else {
		checks = append(checks, fmt.Sprintf("⚠ D7 tool display: %d/%d missing display specs", d7Missing, len(allTools)))
	}

	// D8: backend policy groups consistency (verify group: prefix convention)
	validGroups := 0
	for groupName := range policyGroups {
		if strings.HasPrefix(groupName, "group:") {
			validGroups++
		}
	}
	checks = append(checks, fmt.Sprintf("✓ D8 policy group naming: %d/%d follow 'group:' convention", validGroups, len(policyGroups)))

	// D9: skill bindings
	bindable := tree.BindableTools()
	checks = append(checks, fmt.Sprintf("✓ D9 skill bindings: %d bindable tools", len(bindable)))

	// Registry vs tree comparison
	regNames := make(map[string]bool)
	for _, spec := range Registry {
		regNames[spec.ToolName] = true
	}
	treeNameSet := make(map[string]bool)
	for _, name := range allTools {
		treeNameSet[name] = true
	}
	var missingFromTree []string
	for name := range regNames {
		if !treeNameSet[name] {
			missingFromTree = append(missingFromTree, name)
		}
	}
	sort.Strings(missingFromTree)
	if len(missingFromTree) > 0 {
		checks = append(checks, fmt.Sprintf("⚠ Missing from tree (in registry): %s", strings.Join(missingFromTree, ", ")))
	} else {
		checks = append(checks, "✓ Registry ⊆ Tree: all registry tools present in tree")
	}

	var missingFromRegistry []string
	for name := range treeNameSet {
		if !regNames[name] {
			missingFromRegistry = append(missingFromRegistry, name)
		}
	}
	sort.Strings(missingFromRegistry)
	if len(missingFromRegistry) > 0 {
		checks = append(checks, fmt.Sprintf("ℹ Tree-only tools (not in registry): %s", strings.Join(missingFromRegistry, ", ")))
	}

	dr := DiagnoseResult{
		Inventory: inventory,
		Checks:    checks,
	}
	return ManageResult{Action: "diagnose", Success: true, Data: dr}
}

// ---------------------------------------------------------------------------
// P4-6: action=generate_prompt — Generate prompt section for a tier
// ---------------------------------------------------------------------------

func executeManageGeneratePrompt(tree *CapabilityTree, input ManageInput) ManageResult {
	if input.Tier == "" {
		return ManageResult{Action: "generate_prompt", Success: false, Error: "tier is required for generate_prompt action"}
	}
	if tierIndex(input.Tier) < 0 {
		return ManageResult{Action: "generate_prompt", Success: false, Error: fmt.Sprintf("invalid tier %q", input.Tier)}
	}

	// Get tools available at this tier
	nodes := tree.ToolsForTier(input.Tier)
	if len(nodes) == 0 {
		return ManageResult{Action: "generate_prompt", Success: true, Data: fmt.Sprintf("(no tools available at tier %q)", input.Tier)}
	}

	// Sort by SortOrder
	type entry struct {
		name    string
		summary string
		order   int
	}
	var entries []entry
	for _, n := range nodes {
		summary := ""
		order := 999
		if n.Prompt != nil {
			summary = n.Prompt.Summary
			order = n.Prompt.SortOrder
		}
		entries = append(entries, entry{name: n.Name, summary: summary, order: order})
	}
	sort.Slice(entries, func(i, j int) bool {
		if entries[i].order != entries[j].order {
			return entries[i].order < entries[j].order
		}
		return entries[i].name < entries[j].name
	})

	var sb strings.Builder
	sb.WriteString(fmt.Sprintf("## Tooling (tier: %s)\n\n", input.Tier))
	for _, e := range entries {
		if e.summary != "" {
			sb.WriteString(fmt.Sprintf("- **%s**: %s\n", e.name, e.summary))
		} else {
			sb.WriteString(fmt.Sprintf("- **%s**\n", e.name))
		}
	}

	return ManageResult{Action: "generate_prompt", Success: true, Data: sb.String()}
}

