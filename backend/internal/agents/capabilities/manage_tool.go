// ---------------------------------------------------------------------------
// P7-5: action=apply_patch — Apply an approved patch to the tree
// ---------------------------------------------------------------------------

func executeManageApplyPatch(tree *CapabilityTree, input ManageInput) ManageResult {
	if input.PatchID == "" {
		return ManageResult{Action: "apply_patch", Success: false, Error: "patch_id is required for apply_patch"}
	}

	patch := loadPatch(input.PatchID)
	if patch == nil {
		return ManageResult{Action: "apply_patch", Success: false, Error: fmt.Sprintf("patch %q not found", input.PatchID)}
	}
	if patch.Status == "applied" {
		return ManageResult{Action: "apply_patch", Success: false, Error: fmt.Sprintf("patch %q already applied", input.PatchID)}
	}

	// P7-11: Approval check — apply_patch requires exec_escalation approval
	if !input.Approved {
		return ManageResult{
			Action:  "apply_patch",
			Success: false,
			Error:   "apply_patch requires exec_escalation approval; set approved=true after obtaining approval",
		}
	}

	// Apply operations to tree under mutation lock.
	// On failure, rebuild the tree from scratch to avoid partial state.
	treeMutationMu.Lock()
	for _, op := range patch.Operations {
		if err := applyOperation(tree, op); err != nil {
			// Rollback: rebuild tree to discard partial modifications
			defaultTree = GenerateTreeFromRegistry()
			treeMutationMu.Unlock()
			return ManageResult{
				Action:  "apply_patch",
				Success: false,
				Error:   fmt.Sprintf("failed to apply operation (op=%s, path=%s): %v; tree rolled back", op.Op, op.Path, err),
			}
		}
	}
	treeMutationMu.Unlock()

	patchStoreMu.Lock()
	patch.Status = "applied"
	patchStoreMu.Unlock()

	// P7-7: Auto-trigger validate L1-L3
	validateResult := executeManageValidate(tree, ManageInput{Level: 0})

	// P7-8: Derivation pipeline regeneration info
	derivation := map[string]string{
		"status":  "in-memory tree updated; derivation targets will reflect changes on next read",
		"action":  "Run 'go generate ./internal/agents/capabilities/...' to regenerate frontend artifacts",
		"targets": "D1(prompt) D3(allowlist) D5(policy) D6(ts) D7(display) D8(wizard) D9(registry)",
	}

	return ManageResult{
		Action:  "apply_patch",
		Success: true,
		Data: map[string]interface{}{
			"patch":      patch,
			"validation": validateResult.Data,
			"derivation": derivation,
		},
	}
}

// applyOperation applies a single PatchOperation to the tree.
func applyOperation(tree *CapabilityTree, op PatchOperation) error {
	switch op.Op {
	case "add":
		var node CapabilityNode
		if err := json.Unmarshal(op.Value, &node); err != nil {
			return fmt.Errorf("unmarshal node for add: %w", err)
		}
		return tree.AddNode(&node)
	case "replace":
		node := tree.Lookup(op.Path)
		if node == nil {
			return fmt.Errorf("node %q not found for replace", op.Path)
		}
		return applyFieldReplace(node, op.Field, op.Value)
	case "remove":
		return tree.RemoveNode(op.Path)
	default:
		return fmt.Errorf("unknown op %q", op.Op)
	}
}

// applyFieldReplace applies a field-level replacement to a node.
func applyFieldReplace(node *CapabilityNode, field string, value json.RawMessage) error {
	switch field {
	case "prompt":
		var v NodePrompt
		if err := json.Unmarshal(value, &v); err != nil {
			return err
		}
		node.Prompt = &v
	case "routing":
		var v NodeRouting
		if err := json.Unmarshal(value, &v); err != nil {
			return err
		}
		node.Routing = &v
	case "runtime":
		var v NodeRuntime
		if err := json.Unmarshal(value, &v); err != nil {
			return err
		}
		node.Runtime = &v
	case "perms":
		var v NodePermissions
		if err := json.Unmarshal(value, &v); err != nil {
			return err
		}
		node.Perms = &v
	case "skills":
		var v NodeSkillBinding
		if err := json.Unmarshal(value, &v); err != nil {
			return err
		}
		node.Skills = &v
	case "display":
		var v NodeDisplay
		if err := json.Unmarshal(value, &v); err != nil {
			return err
		}
		node.Display = &v
	case "policy":
		var v NodePolicy
		if err := json.Unmarshal(value, &v); err != nil {
			return err
		}
		node.Policy = &v
	default:
		return fmt.Errorf("unknown field %q", field)
	}
	return nil
}
