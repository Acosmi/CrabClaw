// P7-3: action=propose_routing — Propose routing rule changes
// ---------------------------------------------------------------------------

func executeManageProposeRouting(tree *CapabilityTree, input ManageInput) ManageResult {
	if input.NodeID == "" {
		return ManageResult{Action: "propose_routing", Success: false, Error: "node_id is required for propose_routing"}
	}
	if len(input.Updates) == 0 {
		return ManageResult{Action: "propose_routing", Success: false, Error: "updates (NodeRouting fields) is required for propose_routing"}
	}

	node := tree.Lookup(input.NodeID)
	if node == nil {
		node = tree.LookupByName(input.NodeID)
	}
	if node == nil {
		return ManageResult{Action: "propose_routing", Success: false, Error: fmt.Sprintf("node %q not found", input.NodeID)}
	}

	var newRouting NodeRouting
	if err := json.Unmarshal(input.Updates, &newRouting); err != nil {
		return ManageResult{Action: "propose_routing", Success: false, Error: fmt.Sprintf("invalid routing updates: %v", err)}
	}
	if newRouting.MinTier != "" && tierIndex(newRouting.MinTier) < 0 {
		return ManageResult{Action: "propose_routing", Success: false, Error: fmt.Sprintf("invalid min_tier %q", newRouting.MinTier)}
	}

	oldJSON, _ := json.Marshal(node.Routing)
	newJSON, _ := json.Marshal(&newRouting)

	patch := &TreePatch{
		ID:          nextPatchID(),
		Action:      "routing",
		Description: fmt.Sprintf("Update routing for %q", node.ID),
		Operations: []PatchOperation{{
			Op: "replace", Path: node.ID, Field: "routing", Value: newJSON, Old: oldJSON,
		}},
		CreatedAt: time.Now().Format(time.RFC3339),
		Status:    "proposed",
	}
	storePatch(patch)

	// Generate intent_router.go change suggestion
	var suggestion string
	if len(newRouting.IntentKeywords.ZH) > 0 || len(newRouting.IntentKeywords.EN) > 0 {
		suggestion = fmt.Sprintf(
			"intent_router.go: Tool %q routing updated. IntentKeywords are automatically derived via TreeClassificationKeywords(); no manual code change needed.",
			node.Name,
		)
	}
	if newRouting.MinTier != "" && (node.Routing == nil || newRouting.MinTier != node.Routing.MinTier) {
		tierNote := fmt.Sprintf("MinTier changed to %q — tool will be available from this tier onwards.", newRouting.MinTier)
		if suggestion != "" {
			suggestion += " " + tierNote
		} else {
			suggestion = tierNote
		}
	}

	result := map[string]interface{}{"patch": patch}
	if suggestion != "" {
		result["intent_router_suggestion"] = suggestion
	}
	return ManageResult{Action: "propose_routing", Success: true, Data: result}
}

