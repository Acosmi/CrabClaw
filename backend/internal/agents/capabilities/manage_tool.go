// TreePatch represents a proposed change to the capability tree.
// Generated by propose_* actions, consumed by apply_patch.
type TreePatch struct {
	ID          string           `json:"id"`
	Action      string           `json:"action"` // "register"|"update"|"routing"|"binding"
	Description string           `json:"description"`
	Operations  []PatchOperation `json:"operations"`
	CreatedAt   string           `json:"created_at"`
	Status      string           `json:"status"` // "proposed"|"applied"
}

// PatchOperation is a single operation within a TreePatch.
type PatchOperation struct {
	Op    string          `json:"op"`              // "add"|"replace"|"remove"
	Path  string          `json:"path"`            // Node ID path (e.g., "fs/new_tool")
	Field string          `json:"field,omitempty"` // Sub-structure: "prompt"|"routing"|"skills"|etc.
	Value json.RawMessage `json:"value,omitempty"` // New value
	Old   json.RawMessage `json:"old,omitempty"`   // Previous value (informational)
}

// ProposeNodeSpec is the input for propose_register.
type ProposeNodeSpec struct {
	Name    string            `json:"name"`
	Parent  string            `json:"parent"`
	Kind    NodeKind          `json:"kind"`
	Runtime *NodeRuntime      `json:"runtime,omitempty"`
	Prompt  *NodePrompt       `json:"prompt,omitempty"`
	Routing *NodeRouting      `json:"routing,omitempty"`
	Perms   *NodePermissions  `json:"perms,omitempty"`
	Skills  *NodeSkillBinding `json:"skills,omitempty"`
	Display *NodeDisplay      `json:"display,omitempty"`
	Policy  *NodePolicy       `json:"policy,omitempty"`
}

// ---------------------------------------------------------------------------
// P7: In-memory patch store
// ---------------------------------------------------------------------------

var (
	patchStore   = make(map[string]*TreePatch)
	patchStoreMu sync.RWMutex
)

func storePatch(p *TreePatch) {
	patchStoreMu.Lock()
	defer patchStoreMu.Unlock()
	patchStore[p.ID] = p
}

func loadPatch(id string) *TreePatch {
	patchStoreMu.RLock()
	defer patchStoreMu.RUnlock()
	return patchStore[id]
}

func nextPatchID() string {
	return fmt.Sprintf("patch-%d", time.Now().UnixNano())
}

// ClearPatchStoreForTesting resets the patch store. Test-only.
func ClearPatchStoreForTesting() {
	patchStoreMu.Lock()
	defer patchStoreMu.Unlock()
	patchStore = make(map[string]*TreePatch)
}

