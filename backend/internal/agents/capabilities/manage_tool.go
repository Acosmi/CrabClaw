// ValidationIssue represents a single validation finding.
type ValidationIssue struct {
	Level   int    `json:"level"`   // 1, 2, or 3
	NodeID  string `json:"node_id"` // Affected node (empty for L3 system issues)
	Message string `json:"message"`
}

// ValidationResult holds the output of the validate action.
type ValidationResult struct {
	Level1Pass bool              `json:"level1_pass"`
	Level2Pass bool              `json:"level2_pass"`
	Level3Pass bool              `json:"level3_pass"`
	Issues     []ValidationIssue `json:"issues"`
}

func executeManageValidate(tree *CapabilityTree, input ManageInput) ManageResult {
	vr := ValidationResult{Level1Pass: true, Level2Pass: true, Level3Pass: true}
	runL1 := input.Level == 0 || input.Level == 1
	runL2 := input.Level == 0 || input.Level == 2
	runL3 := input.Level == 0 || input.Level == 3

	if runL1 {
		issues := validateLevel1(tree)
		if len(issues) > 0 {
			vr.Level1Pass = false
			vr.Issues = append(vr.Issues, issues...)
		}
	}
	if runL2 {
		issues := validateLevel2(tree)
		if len(issues) > 0 {
			vr.Level2Pass = false
			vr.Issues = append(vr.Issues, issues...)
		}
	}
	if runL3 {
		issues := validateLevel3(tree)
		if len(issues) > 0 {
			vr.Level3Pass = false
			vr.Issues = append(vr.Issues, issues...)
		}
	}

	return ManageResult{Action: "validate", Success: true, Data: vr}
}

// validateLevel1 checks per-node self-consistency (§6.3 L1).
func validateLevel1(tree *CapabilityTree) []ValidationIssue {
	var issues []ValidationIssue
	tree.Walk(func(n *CapabilityNode) bool {
		if n.Kind == NodeKindGroup {
			return true
		}
		// L1-1: Summary non-empty
		if n.Prompt == nil || n.Prompt.Summary == "" {
			issues = append(issues, ValidationIssue{Level: 1, NodeID: n.ID, Message: "Prompt.Summary is empty"})
		}
		// L1-2: MinTier valid
		if n.Routing == nil || n.Routing.MinTier == "" {
			issues = append(issues, ValidationIssue{Level: 1, NodeID: n.ID, Message: "Routing.MinTier is empty"})
		} else if tierIndex(n.Routing.MinTier) < 0 {
			issues = append(issues, ValidationIssue{Level: 1, NodeID: n.ID, Message: fmt.Sprintf("Routing.MinTier %q is not a valid tier", n.Routing.MinTier)})
		}
		// L1-3: Non-dynamic nodes must have Runtime.Owner
		if n.Runtime == nil || (!n.Runtime.Dynamic && n.Runtime.Owner == "") {
			issues = append(issues, ValidationIssue{Level: 1, NodeID: n.ID, Message: "Runtime.Owner is empty for non-dynamic node"})
		}
		return true
	})
	return issues
}

// validateLevel2 checks cross-node consistency (§6.3 L2).
func validateLevel2(tree *CapabilityTree) []ValidationIssue {
	var issues []ValidationIssue

	// L2-1: Child MinTier >= parent MinTier
	tree.Walk(func(n *CapabilityNode) bool {
		if n.Kind != NodeKindGroup || n.Routing == nil {
			return true
		}
		parentIdx := tierIndex(n.Routing.MinTier)
		if parentIdx < 0 {
			return true
		}
		for _, childID := range n.Children {
			child := tree.Nodes[childID]
			if child == nil || child.Kind == NodeKindGroup {
				continue
			}
			childMinTier := "task_multimodal"
			if child.Routing != nil && child.Routing.MinTier != "" {
				childMinTier = child.Routing.MinTier
			}
			childIdx := tierIndex(childMinTier)
			if childIdx >= 0 && childIdx < parentIdx {
				issues = append(issues, ValidationIssue{
					Level:   2,
					NodeID:  child.ID,
					Message: fmt.Sprintf("MinTier %q < parent %q MinTier %q", childMinTier, n.ID, n.Routing.MinTier),
				})
			}
		}
		return true
	})

	// L2-2: Dynamic group NamePrefix uniqueness
	prefixMap := make(map[string]string) // prefix → group ID
	for _, g := range tree.DynamicGroups() {
		if g.Runtime == nil || g.Runtime.NamePrefix == "" {
			continue
		}
		if existing, ok := prefixMap[g.Runtime.NamePrefix]; ok {
			issues = append(issues, ValidationIssue{
				Level:   2,
				NodeID:  g.ID,
				Message: fmt.Sprintf("NamePrefix %q overlaps with %q", g.Runtime.NamePrefix, existing),
			})
		}
		prefixMap[g.Runtime.NamePrefix] = g.ID
	}

	return issues
}

// validateLevel3 checks system-level cross-layer consistency (§6.3 L3).
// Note: Some L3 checks require access to runtime state that may not be available
// in this context. We implement what we can with tree-internal data.
func validateLevel3(tree *CapabilityTree) []ValidationIssue {
	var issues []ValidationIssue

	// L3-1: tree.AllStaticTools() vs Registry consistency
	regNames := make(map[string]bool)
	for _, spec := range Registry {
		regNames[spec.ToolName] = true
	}
	treeNames := make(map[string]bool)
	for _, name := range tree.AllStaticTools() {
		treeNames[name] = true
	}
	for name := range regNames {
		if !treeNames[name] {
			issues = append(issues, ValidationIssue{
				Level:   3,
				Message: fmt.Sprintf("Registry tool %q not in tree static tools", name),
			})
		}
	}
	for name := range treeNames {
		if !regNames[name] {
			// Some tools are NOT in Registry — this is expected for runtime-only tools
			// send_email: registered in buildToolDefinitions, not in Registry
			// capability_manage: P4 meta-tool, registered in buildToolDefinitions
			if name != "send_email" && name != "capability_manage" {
				issues = append(issues, ValidationIssue{
					Level:   3,
					Message: fmt.Sprintf("Tree static tool %q not in Registry (may be runtime-only)", name),
				})
			}
		}
	}

	// L3-2: Per-tier allowlist monotonicity
	// Each higher tier must include all tools from lower tiers (minus exclusions)
	for i := 1; i < len(validTiers); i++ {
		lowerTier := validTiers[i-1]
		higherTier := validTiers[i]
		lowerAllow := tree.AllowlistForTier(lowerTier)
		higherAllow := tree.AllowlistForTier(higherTier)
		for name := range lowerAllow {
			if !higherAllow[name] {
				// Check if excluded from the higher tier
				node := tree.LookupByToolHint(name)
				excluded := false
				if node != nil && node.Routing != nil {
					for _, ex := range node.Routing.ExcludeFrom {
						if ex == higherTier {
							excluded = true
							break
						}
					}
				}
				if !excluded {
					issues = append(issues, ValidationIssue{
						Level:   3,
						Message: fmt.Sprintf("Tool %q in %s allowlist but missing from %s (monotonicity violation)", name, lowerTier, higherTier),
					})
				}
			}
		}
	}

	// L3-3: ToolSummaries coverage — every tool/subagent should have a summary
	summaries := tree.ToolSummaries()
	tree.Walk(func(n *CapabilityNode) bool {
		if n.Kind == NodeKindGroup {
			return true
		}
		if _, ok := summaries[n.Name]; !ok {
			issues = append(issues, ValidationIssue{
				Level:   3,
				NodeID:  n.ID,
				Message: fmt.Sprintf("Tool %q has no prompt summary (D1 gap)", n.Name),
			})
		}
		return true
	})

	// L3-4: PolicyGroups — every tool with policy groups should have valid group names
	policyGroups := tree.PolicyGroups()
	for groupName := range policyGroups {
		if !strings.HasPrefix(groupName, "group:") {
			issues = append(issues, ValidationIssue{
				Level:   3,
				Message: fmt.Sprintf("PolicyGroup %q does not follow 'group:*' naming convention", groupName),
			})
		}
	}

	return issues
}

