// ExecuteManageTool dispatches a capability_manage tool call.
// Phase A actions are read-only; Phase B propose_* generate diffs without
// modifying state; apply_patch modifies the in-memory tree (requires approval).
func ExecuteManageTool(inputJSON json.RawMessage) (string, error) {
	var input ManageInput
	if err := json.Unmarshal(inputJSON, &input); err != nil {
		return formatManageError("", fmt.Sprintf("invalid input: %v", err)), nil
	}

	tree := DefaultTree()

	var result ManageResult
	switch input.Action {
	case "tree":
		result = executeManageTree(tree, input)
	case "inspect":
		result = executeManageInspect(tree, input)
	case "validate":
		result = executeManageValidate(tree, input)
	case "diagnose":
		result = executeManageDiagnose(tree)
	case "generate_prompt":
		result = executeManageGeneratePrompt(tree, input)
	case "generate_allowlist":
		result = executeManageGenerateAllowlist(tree, input)
	// P7: write-path actions
	case "propose_register":
		result = executeManageProposeRegister(tree, input)
	case "propose_update":
		result = executeManageProposeUpdate(tree, input)
	case "propose_routing":
		result = executeManageProposeRouting(tree, input)
	case "propose_binding":
		result = executeManageProposeBinding(tree, input)
	case "apply_patch":
		result = executeManageApplyPatch(tree, input)
	default:
		return formatManageError(input.Action, fmt.Sprintf("unknown action %q; valid actions: tree, inspect, validate, diagnose, generate_prompt, generate_allowlist, propose_register, propose_update, propose_routing, propose_binding, apply_patch", input.Action)), nil
	}

	data, err := json.MarshalIndent(result, "", "  ")
	if err != nil {
		return formatManageError(input.Action, fmt.Sprintf("marshal result: %v", err)), nil
	}
	return string(data), nil
}

func formatManageError(action, msg string) string {
	r := ManageResult{Action: action, Success: false, Error: msg}
	data, _ := json.MarshalIndent(r, "", "  ")
	return string(data)
}

