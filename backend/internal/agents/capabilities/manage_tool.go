// manage_tool.go implements the capability_manage meta-tool (Phase A + Phase B).
//
// Phase A (read-only):
//   - tree:               View capability tree structure (subtree filter, depth control)
//   - inspect:            View single node metadata (all 7 dimensions)
//   - validate:           Three-level validation (L1 Node → L2 Cross-Node → L3 System)
//   - diagnose:           Drift diagnosis (tree vs 9 consumers)
//   - generate_prompt:    Generate prompt section for a given tier
//   - generate_allowlist: Generate tool allowlist for a given tier
//
// Phase B (P7, patch-based write paths):
//   - propose_register:   Propose registering a new tool node
//   - propose_update:     Propose updating node fields
//   - propose_routing:    Propose routing rule changes
//   - propose_binding:    Propose skill binding changes
//   - apply_patch:        Apply approved patch (requires exec_escalation)
//
// Design doc: docs/codex/2026-03-09-能力树与自治能力管理系统架构设计-v2.md §6
package capabilities

