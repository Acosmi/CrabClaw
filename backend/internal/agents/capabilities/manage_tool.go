// TreeNodeView is a compact view of a tree node for the tree action.
type TreeNodeView struct {
	ID       string          `json:"id"`
	Name     string          `json:"name"`
	Kind     NodeKind        `json:"kind"`
	MinTier  string          `json:"min_tier,omitempty"`
	Summary  string          `json:"summary,omitempty"`
	Dynamic  bool            `json:"dynamic,omitempty"`
	Children []*TreeNodeView `json:"children,omitempty"`
}

func executeManageTree(tree *CapabilityTree, input ManageInput) ManageResult {
	if input.NodeID != "" {
		// Subtree mode
		root := tree.Lookup(input.NodeID)
		if root == nil {
			return ManageResult{Action: "tree", Success: false, Error: fmt.Sprintf("node %q not found", input.NodeID)}
		}
		view := buildTreeView(tree, root, input.Depth, 0)
		return ManageResult{Action: "tree", Success: true, Data: view}
	}

	// Full tree: build from root children
	var roots []*TreeNodeView
	for _, id := range tree.RootChildren {
		node := tree.Nodes[id]
		if node == nil {
			continue
		}
		roots = append(roots, buildTreeView(tree, node, input.Depth, 0))
	}
	return ManageResult{Action: "tree", Success: true, Data: roots}
}

func buildTreeView(tree *CapabilityTree, node *CapabilityNode, maxDepth, currentDepth int) *TreeNodeView {
	view := &TreeNodeView{
		ID:   node.ID,
		Name: node.Name,
		Kind: node.Kind,
	}
	if node.Routing != nil && node.Routing.MinTier != "" {
		view.MinTier = node.Routing.MinTier
	}
	if node.Prompt != nil && node.Prompt.Summary != "" {
		view.Summary = node.Prompt.Summary
	}
	if node.Runtime != nil && node.Runtime.Dynamic {
		view.Dynamic = true
	}

	// Recurse into children (depth-limited)
	if maxDepth > 0 && currentDepth >= maxDepth {
		return view
	}
	for _, childID := range node.Children {
		child := tree.Nodes[childID]
		if child == nil {
			continue
		}
		view.Children = append(view.Children, buildTreeView(tree, child, maxDepth, currentDepth+1))
	}
	return view
}

// ---------------------------------------------------------------------------
// P4-3: action=inspect — View single node metadata (7 dimensions)
// ---------------------------------------------------------------------------

func executeManageInspect(tree *CapabilityTree, input ManageInput) ManageResult {
	if input.NodeID == "" {
		return ManageResult{Action: "inspect", Success: false, Error: "node_id is required for inspect action"}
	}
	node := tree.Lookup(input.NodeID)
	if node == nil {
		// Try lookup by name
		node = tree.LookupByName(input.NodeID)
	}
	if node == nil {
		return ManageResult{Action: "inspect", Success: false, Error: fmt.Sprintf("node %q not found", input.NodeID)}
	}
	return ManageResult{Action: "inspect", Success: true, Data: node}
}

