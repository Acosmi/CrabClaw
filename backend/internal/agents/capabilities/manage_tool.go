// ---------------------------------------------------------------------------
// P7-1: action=propose_register — Propose registering a new tool node
// ---------------------------------------------------------------------------

func executeManageProposeRegister(tree *CapabilityTree, input ManageInput) ManageResult {
	if input.NodeSpec == nil {
		return ManageResult{Action: "propose_register", Success: false, Error: "node_spec is required for propose_register"}
	}
	spec := input.NodeSpec
	if spec.Name == "" {
		return ManageResult{Action: "propose_register", Success: false, Error: "node_spec.name is required"}
	}
	if spec.Parent == "" {
		return ManageResult{Action: "propose_register", Success: false, Error: "node_spec.parent is required"}
	}
	if spec.Kind == "" {
		spec.Kind = NodeKindTool
	}

	nodeID := spec.Parent + "/" + spec.Name

	if tree.Lookup(nodeID) != nil {
		return ManageResult{Action: "propose_register", Success: false, Error: fmt.Sprintf("node %q already exists", nodeID)}
	}
	if tree.Lookup(spec.Parent) == nil {
		return ManageResult{Action: "propose_register", Success: false, Error: fmt.Sprintf("parent %q not found", spec.Parent)}
	}

	node := &CapabilityNode{
		ID: nodeID, Name: spec.Name, Kind: spec.Kind, Parent: spec.Parent,
		Runtime: spec.Runtime, Prompt: spec.Prompt, Routing: spec.Routing,
		Perms: spec.Perms, Skills: spec.Skills, Display: spec.Display, Policy: spec.Policy,
	}

	// Fill safe defaults for missing dimensions
	if node.Runtime == nil {
		node.Runtime = &NodeRuntime{Owner: "attempt_runner", EnabledWhen: "always"}
	}
	if node.Prompt == nil {
		node.Prompt = &NodePrompt{Summary: spec.Name + " tool"}
	}
	if node.Routing == nil {
		node.Routing = &NodeRouting{MinTier: "task_write"}
	}
	if node.Perms == nil {
		node.Perms = &NodePermissions{MinSecurityLevel: "allowlist", FileAccess: "none", ApprovalType: "none", ScopeCheck: "none"}
	}
	if node.Skills == nil {
		node.Skills = &NodeSkillBinding{Bindable: true}
	}

	nodeJSON, _ := json.Marshal(node)

	patch := &TreePatch{
		ID:          nextPatchID(),
		Action:      "register",
		Description: fmt.Sprintf("Register new %s %q under %q", spec.Kind, spec.Name, spec.Parent),
		Operations: []PatchOperation{{
			Op: "add", Path: nodeID, Value: nodeJSON,
		}},
		CreatedAt: time.Now().Format(time.RFC3339),
		Status:    "proposed",
	}
	storePatch(patch)
	return ManageResult{Action: "propose_register", Success: true, Data: patch}
}

// ---------------------------------------------------------------------------
// P7-2: action=propose_update — Propose updating node fields
// ---------------------------------------------------------------------------

func executeManageProposeUpdate(tree *CapabilityTree, input ManageInput) ManageResult {
	if input.NodeID == "" {
		return ManageResult{Action: "propose_update", Success: false, Error: "node_id is required for propose_update"}
	}
	if len(input.Updates) == 0 {
		return ManageResult{Action: "propose_update", Success: false, Error: "updates is required for propose_update"}
	}

	node := tree.Lookup(input.NodeID)
	if node == nil {
		node = tree.LookupByName(input.NodeID)
	}
	if node == nil {
		return ManageResult{Action: "propose_update", Success: false, Error: fmt.Sprintf("node %q not found", input.NodeID)}
	}

	var updates ProposeNodeSpec
	if err := json.Unmarshal(input.Updates, &updates); err != nil {
		return ManageResult{Action: "propose_update", Success: false, Error: fmt.Sprintf("invalid updates: %v", err)}
	}

	ops := buildUpdateOps(node, updates)
	if len(ops) == 0 {
		return ManageResult{Action: "propose_update", Success: false, Error: "no updatable fields found in updates"}
	}

	patch := &TreePatch{
		ID:          nextPatchID(),
		Action:      "update",
		Description: fmt.Sprintf("Update %d field(s) on %q", len(ops), node.ID),
		Operations:  ops,
		CreatedAt:   time.Now().Format(time.RFC3339),
		Status:      "proposed",
	}
	storePatch(patch)
	return ManageResult{Action: "propose_update", Success: true, Data: patch}
}

// buildUpdateOps generates PatchOperations for each non-nil dimension in the update spec.
func buildUpdateOps(node *CapabilityNode, updates ProposeNodeSpec) []PatchOperation {
	var ops []PatchOperation
	if updates.Prompt != nil {
		oldJSON, _ := json.Marshal(node.Prompt)
		newJSON, _ := json.Marshal(updates.Prompt)
		ops = append(ops, PatchOperation{Op: "replace", Path: node.ID, Field: "prompt", Value: newJSON, Old: oldJSON})
	}
	if updates.Runtime != nil {
		oldJSON, _ := json.Marshal(node.Runtime)
		newJSON, _ := json.Marshal(updates.Runtime)
		ops = append(ops, PatchOperation{Op: "replace", Path: node.ID, Field: "runtime", Value: newJSON, Old: oldJSON})
	}
	if updates.Routing != nil {
		oldJSON, _ := json.Marshal(node.Routing)
		newJSON, _ := json.Marshal(updates.Routing)
		ops = append(ops, PatchOperation{Op: "replace", Path: node.ID, Field: "routing", Value: newJSON, Old: oldJSON})
	}
	if updates.Perms != nil {
		oldJSON, _ := json.Marshal(node.Perms)
		newJSON, _ := json.Marshal(updates.Perms)
		ops = append(ops, PatchOperation{Op: "replace", Path: node.ID, Field: "perms", Value: newJSON, Old: oldJSON})
	}
	if updates.Skills != nil {
		oldJSON, _ := json.Marshal(node.Skills)
		newJSON, _ := json.Marshal(updates.Skills)
		ops = append(ops, PatchOperation{Op: "replace", Path: node.ID, Field: "skills", Value: newJSON, Old: oldJSON})
	}
	if updates.Display != nil {
		oldJSON, _ := json.Marshal(node.Display)
		newJSON, _ := json.Marshal(updates.Display)
		ops = append(ops, PatchOperation{Op: "replace", Path: node.ID, Field: "display", Value: newJSON, Old: oldJSON})
	}
	if updates.Policy != nil {
		oldJSON, _ := json.Marshal(node.Policy)
		newJSON, _ := json.Marshal(updates.Policy)
		ops = append(ops, PatchOperation{Op: "replace", Path: node.ID, Field: "policy", Value: newJSON, Old: oldJSON})
	}
	return ops
}

