// P4-7: action=generate_allowlist — Generate tool allowlist for a tier
// ---------------------------------------------------------------------------

func executeManageGenerateAllowlist(tree *CapabilityTree, input ManageInput) ManageResult {
	if input.Tier == "" {
		return ManageResult{Action: "generate_allowlist", Success: false, Error: "tier is required for generate_allowlist action"}
	}
	if tierIndex(input.Tier) < 0 {
		return ManageResult{Action: "generate_allowlist", Success: false, Error: fmt.Sprintf("invalid tier %q", input.Tier)}
	}

	allowlist := tree.AllowlistForTier(input.Tier)
	// Sort for determinism
	names := make([]string, 0, len(allowlist))
	for name := range allowlist {
		names = append(names, name)
	}
	sort.Strings(names)

	result := map[string]interface{}{
		"tier":  input.Tier,
		"count": len(names),
		"tools": names,
	}
	return ManageResult{Action: "generate_allowlist", Success: true, Data: result}
}

